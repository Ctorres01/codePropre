package ex1;

public class Calcul {

	/**
	 * Méthode qui additionne deux int, et retourne le résultat
	 * @param a: Premier terme de l'addition
	 * @param b: deuxième terme de l'addition
	 * @return Résultat de l'addition a+b
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode qui soustrait deux int et retourne le résultat
	 * @param a: Terme auquel on va soustraire le second
	 * @param b: Terme que l'on va soustraire au premier
	 * @return Résultat de la soustraction a-b
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
